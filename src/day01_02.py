from utils import read_data_generator, get_basename

#-- C O N S T A N T S ---------------------------------------------------------

num_elfs = 3

#-- M A I N   L O O P ---------------------------------------------------------

most_calories = [0] * num_elfs
previous_snacks = []
for snack in read_data_generator(get_basename(__file__)):
    if snack == '\n':
        meal = sum(snack for snack in previous_snacks)
        previous_snacks = []
        if meal > min(most_calories):
            most_calories.remove(min(most_calories))
            most_calories.append(meal)
    else:
        previous_snacks.append(int(snack))
print(sum(most_calories))